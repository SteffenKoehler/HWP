
public class Value {

	String befehl;
	int wert;
	
	public Value(String befehl, int wert){
		this.befehl = befehl;
		this.wert = wert;
	}

	public String getBefehl() {
		return befehl;
	}

	public void setBefehl(String befehl) {
		this.befehl = befehl;
	}

	public int getWert() {
		return wert;
	}

	public void setWert(int wert) {
		this.wert = wert;
	}
	
	
}
